/* ============================================================================
 * EPAL GROUP ERP · tools/build/build-module.mjs
 * ----------------------------------------------------------------------------
 * MODULE FRONTEND BUILD — compiles a module's structured sources into the
 * committed no-build artifact the SPA actually loads.
 *
 *   node tools/build/build-module.mjs companies/travels/modules/passport-mgmt
 *   node tools/build/build-module.mjs --all          # every module with frontend/ sources
 *
 * WHY (owner directive 2026-07-19): "never do one view.js like now" — module
 * frontends are authored as separate, handover-friendly files:
 *
 *   companies/<co>/modules/<id>/frontend/
 *     template.html   ← ALL the screen's markup (Tailwind tw- utilities),
 *                       <template data-tpl="…"> fragments for repeated rows
 *     <id>.js         ← the logic only (data reads, cloning template slots,
 *                       event wiring). NOT an IIFE — the build wraps it.
 *     <id>.css        ← optional module stylesheet for the few rules utilities
 *                       cannot express (custom scrollbars, ::before content…)
 *
 * The build emits the module's  view.js  (AUTO-GENERATED header) — same
 * filename/path as the legacy hand-written screens, so index.html script tags,
 * auto-discovery probes and module.json need NO changes, and the deployed site
 * stays a static git-pull (build output is committed, like tailwind.built.css).
 * The legacy hand-written view.js is preserved by git history (R4).
 *
 * Inside the wrapper the logic file sees two constants:
 *   TEMPLATE_HTML — the raw contents of template.html (string)
 *   MODULE_CSS    — the stylesheet contents, or null (injected once into
 *                   <head> as <style data-module-style="<co>/<id>"> before
 *                   the logic runs)
 * ==========================================================================*/
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..', '..');

function buildModule(modDir) {
  const abs = path.resolve(ROOT, modDir);
  const fe = path.join(abs, 'frontend');
  const id = path.basename(abs);
  const company = path.basename(path.dirname(path.dirname(abs)));   // companies/<co>/modules/<id>
  const tplPath = path.join(fe, 'template.html');
  const jsPath = path.join(fe, id + '.js');
  const cssPath = path.join(fe, id + '.css');

  if (!fs.existsSync(tplPath) || !fs.existsSync(jsPath)) {
    return { modDir, skipped: true, reason: 'no frontend/template.html + frontend/' + id + '.js' };
  }
  const tpl = fs.readFileSync(tplPath, 'utf8');
  const logic = fs.readFileSync(jsPath, 'utf8');
  const css = fs.existsSync(cssPath) ? fs.readFileSync(cssPath, 'utf8') : null;
  const key = company + '/' + id;

  const out = [
    '/* ============================================================================',
    ' * AUTO-GENERATED by tools/build/build-module.mjs — DO NOT EDIT THIS FILE.',
    ' * Sources (edit these, then rebuild):',
    ' *   frontend/template.html   — the markup',
    ' *   frontend/' + id + '.js   — the logic',
    css !== null ? ' *   frontend/' + id + '.css  — the module stylesheet' : ' *   (no module stylesheet)',
    ' * Rebuild:  node tools/build/build-module.mjs ' + modDir.replace(/\\/g, '/'),
    ' * ==========================================================================*/',
    '(function () {',
    "  'use strict';",
    '  var TEMPLATE_HTML = ' + JSON.stringify(tpl) + ';',
    '  var MODULE_CSS = ' + (css === null ? 'null' : JSON.stringify(css)) + ';',
    '  if (MODULE_CSS && !document.querySelector(\'style[data-module-style="' + key + '"]\')) {',
    "    var st = document.createElement('style');",
    "    st.setAttribute('data-module-style', '" + key + "');",
    '    st.textContent = MODULE_CSS;',
    '    document.head.appendChild(st);',
    '  }',
    '  /* ---- frontend/' + id + '.js ---------------------------------------- */',
    logic,
    '})();',
    '',
  ].join('\n');

  fs.writeFileSync(path.join(abs, 'view.js'), out);
  return { modDir, built: true, bytes: out.length };
}

const arg = process.argv[2];
if (!arg) { console.error('usage: build-module.mjs <module-dir> | --all'); process.exit(2); }

let targets = [];
if (arg === '--all') {
  for (const co of fs.readdirSync(path.join(ROOT, 'companies'))) {
    const mods = path.join(ROOT, 'companies', co, 'modules');
    if (!fs.existsSync(mods)) continue;
    for (const m of fs.readdirSync(mods)) {
      if (fs.existsSync(path.join(mods, m, 'frontend', 'template.html'))) targets.push(path.join('companies', co, 'modules', m));
    }
  }
} else targets = [arg];

for (const t of targets) {
  const r = buildModule(t);
  console.log(r.built ? `built  ${r.modDir} → view.js (${r.bytes} bytes)` : `skip   ${r.modDir} — ${r.reason}`);
}
