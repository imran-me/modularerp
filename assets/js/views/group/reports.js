/* ============================================================================
 * EPAL GROUP ERP  ·  views/group/reports.js
 * ----------------------------------------------------------------------------
 * GROUP REPORT CENTER (route: group/reports) — the owner's documentation desk.
 *
 * A card grid of five board-ready reports, each generated LIVE from the data
 * layer at the moment of download (no stale snapshots):
 *   1. Consolidated P&L        — all concerns side by side, month by month.
 *   2. Group Sales Register    — every sale across the group with company tag.
 *   3. Workforce Roster        — the full employee book with payroll totals.
 *   4. Company Comparison      — the groupSnapshot() health matrix.
 *   5. Payables & Receivables  — open acc_schedules, aged and totalled.
 *
 * Reports render as self-contained HTML documents (print-ready) or CSV via a
 * Blob download. Data: db.financials, db.sales, db.employees, db.groupSnapshot
 * and the acc_schedules store (see docs CONTRACT for shapes).
 * ==========================================================================*/

(function (EPAL) {
  'use strict';
  var ui = EPAL.ui, el = ui.el;
  var db = function () { return EPAL.db; };

  /* ---- tiny shared helpers ------------------------------------------------*/
  function kpi(label, value, icon, foot) {
    return el('div.kpi-card', null, [
      el('div.kpi-top', null, [ el('span.kpi-label', { text: label }),
        el('span.kpi-ico', { html: '<i class="bi bi-' + icon + '"></i>' }) ]),
      el('div.kpi-value', { text: String(value) }),
      foot ? el('div.kpi-foot', null, [ el('span.text-muted', { text: foot }) ]) : null
    ]);
  }
  function chartCard(title, icon, canvasId, subLabel, height) {
    return el('div.card', null, [
      el('div.card-head', null, [ el('h3', { html: ui.icon(icon) + ' ' + title }),
        subLabel ? el('span.card-sub', { text: subLabel }) : null ]),
      el('div.card-body', null, [
        el('div', { style: { height: (height || 260) + 'px', position: 'relative' } }, [ el('canvas', { id: canvasId }) ])
      ])
    ]);
  }
  function activeCompanies() {
    return EPAL.config.companies.filter(function (c) { return c.type === 'company' && c.enabled; });
  }
  function esc(s) { return ui.escapeHtml(s); }
  function mLabel(ym) {
    var p = String(ym).split('-');
    return new Date(+p[0], +p[1] - 1, 1).toLocaleString('en', { month: 'short', year: '2-digit' });
  }
  function dl(name, content, mime) {
    var blob = new Blob([content], { type: mime || 'text/html' });
    var a = el('a', { href: URL.createObjectURL(blob), download: name });
    document.body.appendChild(a); a.click(); a.remove();
    ui.toast('Downloaded ' + name, 'success');
  }
  function htmlDoc(title, bodyHtml) {
    return '<!doctype html><html><head><meta charset="utf-8"><title>' + esc(title) + '</title><style>' +
      'body{font-family:Inter,Arial,sans-serif;color:#111;max-width:960px;margin:36px auto;padding:0 20px}' +
      'h1{margin:0;font-size:24px}h2{border-bottom:2px solid #2591D9;padding-bottom:6px;margin-top:26px;font-size:15px}' +
      'table{width:100%;border-collapse:collapse;margin-top:10px}td,th{border:1px solid #ddd;padding:7px 9px;text-align:left;font-size:12.5px}' +
      'th{background:#f6f6f6}.num{text-align:right;font-variant-numeric:tabular-nums}.strong{font-weight:700}' +
      'tr.total td{font-weight:700;background:#faf6ec}' +
      '.head{display:flex;justify-content:space-between;align-items:center;border-bottom:3px solid #2591D9;padding-bottom:12px}' +
      '.muted{color:#666;font-size:12.5px}.brand{font-weight:800;color:#2591D9;font-size:14px}</style></head><body>' +
      '<div class="head"><div><h1>' + esc(title) + '</h1><div class="muted">' + esc(EPAL.config.group.legalName) +
      ' · generated ' + ui.date(new Date(), 'full') + '</div></div>' +
      '<div class="brand">EPAL GROUP</div></div>' + bodyHtml +
      '<p class="muted" style="margin-top:28px">Generated by Epal Group ERP · Consolidated across all sister concerns · Confidential.</p></body></html>';
  }
  function coShort(cid) {
    var co = EPAL.config.company(cid);
    return co ? co.short : (cid || '—');
  }

  /* ==========================================================================
   * VIEW
   * ========================================================================*/
  EPAL.view('group/reports', { render: function (ctx) {
    var page = el('div.page');
    var comps = activeCompanies();
    var sales = db().sales();
    var employees = db().employees();
    var schedules = db().col('acc_schedules');
    var fins = db().financials();
    var customers = db().customers();
    var openSch = schedules.filter(function (s) { return s.status !== 'Paid'; });
    var recordsCovered = sales.length + employees.length + schedules.length + fins.length + customers.length;

    page.appendChild(EPAL.pageHead({
      eyebrow: 'Epal Group · Command Layer', icon: 'file-earmark-bar-graph-fill',
      title: 'Group Report Center',
      sub: 'Board-ready, print-ready consolidated reports — every document is generated live from the data layer the moment you download it.',
      actions: [
        el('button.btn.btn-ghost', { html: ui.icon('cash-coin') + ' Consolidated Finance',
          onclick: function () { EPAL.router.navigate('group/finance'); } })
      ]
    }));

    /* ---- KPI row ------------------------------------------------------- */
    page.appendChild(el('div.kpi-grid.stagger', null, [
      kpi('Reports Available', 5, 'files', 'consolidated group documents'),
      kpi('Data Freshness', ui.date(new Date(), 'time'), 'lightning-charge-fill', 'read live at download time'),
      kpi('Records Covered', ui.num(recordsCovered), 'database-fill', 'sales · people · schedules · financials'),
      kpi('Active Concerns', comps.length, 'diagram-3-fill', comps.map(function (c) { return c.short; }).join(' · '))
    ]));

    /* ---- report cards ---------------------------------------------------*/
    var defs = [
      { id: 'pnl', title: 'Consolidated P&L', icon: 'graph-up-arrow',
        desc: 'All concerns side by side — monthly revenue and profit matrices plus a 12-month summary.',
        meta: '12 months × ' + comps.length + ' concerns' },
      { id: 'sales', title: 'Group Sales Register', icon: 'receipt',
        desc: 'Every recorded sale across the group with company, customer, cost and profit.',
        meta: ui.num(sales.length) + ' sales rows' },
      { id: 'roster', title: 'Workforce Roster', icon: 'person-badge-fill',
        desc: 'The full employee book — designation, department, salary and status per concern.',
        meta: ui.num(employees.length) + ' employees' },
      { id: 'compare', title: 'Company Comparison Snapshot', icon: 'diagram-3-fill',
        desc: 'Revenue, margin, momentum, risk and headcount for every sister concern in one matrix.',
        meta: comps.length + ' concerns compared' },
      { id: 'schedules', title: 'Payables & Receivables Summary', icon: 'calendar2-week-fill',
        desc: 'Open payment schedules by company with net position and overdue aging.',
        meta: ui.num(openSch.length) + ' open schedules' }
    ];
    page.appendChild(el('div.section-label', { text: 'Downloadable Reports' }));
    var grid = el('div.grid-auto.stagger');
    defs.forEach(function (d) {
      grid.appendChild(el('div.card.hover', { style: { cursor: 'pointer' }, onclick: function () { runReport(d.id); } }, [
        el('div.card-pad', null, [
          el('div.flex.items-center.gap-2', null, [
            el('div.scaffold-ico', { html: '<i class="bi bi-' + d.icon + '"></i>' }),
            el('div.flex-1', null, [
              el('h4', { text: d.title }),
              el('p.text-mute.sm', { text: d.desc }),
              el('div.text-mute.xs.mt-1', { html: ui.icon('database') + ' ' + esc(d.meta) })
            ]),
            ui.frag('<i class="bi bi-download text-accent"></i>')
          ])
        ])
      ]));
    });
    page.appendChild(grid);

    /* ---- analytics: what the reports draw from --------------------------*/
    page.appendChild(el('div.section-label', { text: 'Data Coverage' }));
    var covId = ui.uid('rcov');
    page.appendChild(chartCard('Records per Data Source', 'bar-chart-steps', covId,
      'live row counts behind the report engine', 240));

    ctx.mount.appendChild(page);

    requestAnimationFrame(function () {
      var c = document.getElementById(covId);
      if (!c) return;
      EPAL.charts.bar(c, {
        labels: ['Sales Ledger', 'Employees', 'Payment Schedules', 'Financial Rows', 'Customers'],
        datasets: [{ label: 'Records',
          data: [sales.length, employees.length, schedules.length, fins.length, customers.length],
          colors: ['#2f6bff', '#7b5cff', '#f4b740', '#2591D9', '#23c17e'] }],
        horizontal: true
      });
    });

    /* ==========================================================================
     * REPORT GENERATORS — everything computed at click time (always fresh).
     * ========================================================================*/
    function runReport(id) {
      if (id === 'pnl') return reportPnl();
      if (id === 'sales') return reportSales();
      if (id === 'roster') return reportRoster();
      if (id === 'compare') return reportCompare();
      if (id === 'schedules') return reportSchedules();
    }

    function reportPnl() {
      var months = db().months(12);
      var map = {};
      db().financials().forEach(function (f) { map[f.companyId + '|' + f.ym] = f; });
      function rv(cid, ym) { var f = map[cid + '|' + ym]; return f ? f.revenue : 0; }
      function ex(cid, ym) { var f = map[cid + '|' + ym]; return f ? f.expense : 0; }

      // 12-month summary per concern + group total
      var sumRows = comps.map(function (c) {
        var f = db().finance(c.id, 12);
        return '<tr><td class="strong">' + esc(c.name) + '</td><td class="num">' + ui.money(f.revenue) +
          '</td><td class="num">' + ui.money(f.expense) + '</td><td class="num">' + ui.money(f.profit) +
          '</td><td class="num">' + ui.pct(f.margin) + '</td></tr>';
      }).join('');
      var g = db().finance(null, 12);
      sumRows += '<tr class="total"><td>Epal Group (consolidated)</td><td class="num">' + ui.money(g.revenue) +
        '</td><td class="num">' + ui.money(g.expense) + '</td><td class="num">' + ui.money(g.profit) +
        '</td><td class="num">' + ui.pct(g.margin) + '</td></tr>';

      function matrix(valFn) {
        var head = '<tr><th>Month</th>' + comps.map(function (c) { return '<th class="num">' + esc(c.short) + '</th>'; }).join('') + '<th class="num">Group</th></tr>';
        var body = months.map(function (ym) {
          var tot = 0;
          var cells = comps.map(function (c) {
            var v = valFn(c.id, ym); tot += v;
            return '<td class="num">' + ui.money(v) + '</td>';
          }).join('');
          return '<tr><td>' + mLabel(ym) + '</td>' + cells + '<td class="num strong">' + ui.money(tot) + '</td></tr>';
        }).join('');
        return '<table>' + head + body + '</table>';
      }

      dl('group-consolidated-pnl.html', htmlDoc('Consolidated Profit & Loss — 12 Months',
        '<h2>Summary by Concern (trailing 12 months)</h2>' +
        '<table><tr><th>Concern</th><th class="num">Revenue</th><th class="num">Expense</th><th class="num">Profit</th><th class="num">Margin</th></tr>' + sumRows + '</table>' +
        '<h2>Monthly Revenue — side by side</h2>' + matrix(rv) +
        '<h2>Monthly Profit — side by side</h2>' + matrix(function (cid, ym) { return rv(cid, ym) - ex(cid, ym); })));
    }

    function reportSales() {
      var rows = db().sales().slice().sort(function (a, b) { return String(b.date).localeCompare(String(a.date)); });
      var totAmt = 0, totProfit = 0;
      var body = rows.map(function (x) {
        totAmt += (x.amount || 0); totProfit += (x.profit || 0);
        return '<tr><td>' + esc(x.id) + '</td><td>' + ui.date(x.date) + '</td><td>' + esc(coShort(x.companyId)) +
          '</td><td>' + esc(x.customer || '—') + '</td><td>' + esc(x.desc || '') +
          '</td><td class="num">' + ui.money(x.amount) + '</td><td class="num">' + ui.money(x.cost) +
          '</td><td class="num">' + ui.money(x.profit) + '</td></tr>';
      }).join('');
      body += '<tr class="total"><td colspan="5">Total · ' + rows.length + ' sales</td><td class="num">' + ui.money(totAmt) +
        '</td><td></td><td class="num">' + ui.money(totProfit) + '</td></tr>';
      dl('group-sales-register.html', htmlDoc('Group Sales Register',
        '<table><tr><th>Ref</th><th>Date</th><th>Company</th><th>Customer</th><th>Description</th>' +
        '<th class="num">Amount</th><th class="num">Cost</th><th class="num">Profit</th></tr>' + body + '</table>'));
    }

    function reportRoster() {
      var rows = db().employees().slice().sort(function (a, b) {
        if (a.companyId !== b.companyId) return String(a.companyId).localeCompare(String(b.companyId));
        return String(a.name).localeCompare(String(b.name));
      });
      var payroll = 0;
      var body = rows.map(function (e) {
        payroll += (e.salary || 0);
        return '<tr><td>' + esc(e.id) + '</td><td class="strong">' + esc(e.name) + '</td><td>' + esc(coShort(e.companyId)) +
          '</td><td>' + esc(e.dept || '') + '</td><td>' + esc(e.designation || '') + '</td><td>' + esc(e.phone || '') +
          '</td><td>' + ui.date(e.joinDate) + '</td><td class="num">' + ui.money(e.salary) +
          '</td><td>' + esc(e.status || '') + '</td></tr>';
      }).join('');
      body += '<tr class="total"><td colspan="7">Total · ' + rows.length + ' employees</td><td class="num">' +
        ui.money(payroll) + '</td><td>monthly payroll</td></tr>';
      dl('group-workforce-roster.html', htmlDoc('Workforce Roster — All Concerns',
        '<table><tr><th>ID</th><th>Employee</th><th>Company</th><th>Department</th><th>Designation</th>' +
        '<th>Phone</th><th>Joined</th><th class="num">Salary</th><th>Status</th></tr>' + body + '</table>'));
    }

    function reportCompare() {
      var snap = db().groupSnapshot();
      var body = snap.companies.map(function (c) {
        return '<tr><td class="strong">' + esc(c.name) + '</td><td class="num">' + ui.money(c.revenue) +
          '</td><td class="num">' + ui.money(c.profit) + '</td><td class="num">' + ui.pct(c.margin) +
          '</td><td class="num">' + (c.mom >= 0 ? '+' : '') + c.mom.toFixed(1) + '%</td><td class="num">' + c.risk +
          ' / 100</td><td class="num">' + c.employees + '</td></tr>';
      }).join('');
      dl('group-company-comparison.html', htmlDoc('Company Comparison Snapshot',
        '<h2>Concern Matrix (trailing 12 months)</h2>' +
        '<table><tr><th>Concern</th><th class="num">Revenue</th><th class="num">Profit</th><th class="num">Margin</th>' +
        '<th class="num">MoM</th><th class="num">Risk Score</th><th class="num">Staff</th></tr>' + body + '</table>' +
        '<h2>Group Totals</h2><table>' +
        '<tr><th>Group Revenue</th><td class="num">' + ui.money(snap.revenue) + '</td></tr>' +
        '<tr><th>Group Profit</th><td class="num">' + ui.money(snap.profit) + '</td></tr>' +
        '<tr><th>Blended Margin</th><td class="num">' + ui.pct(snap.margin) + '</td></tr>' +
        '<tr><th>Headcount</th><td class="num">' + snap.headcount + '</td></tr>' +
        '<tr><th>Customers</th><td class="num">' + snap.customers + '</td></tr>' +
        '<tr><th>Open Leads</th><td class="num">' + snap.openLeads + '</td></tr>' +
        '<tr><th>Pipeline Value</th><td class="num">' + ui.money(snap.pipelineValue) + '</td></tr></table>'));
    }

    function reportSchedules() {
      var all = db().col('acc_schedules');
      var open = all.filter(function (s) { return s.status !== 'Paid'; });
      var byCo = {};
      open.forEach(function (s) {
        var b = byCo[s.companyId] = byCo[s.companyId] || { pay: 0, recv: 0 };
        if (s.kind === 'Payable') b.pay += (s.amount || 0); else b.recv += (s.amount || 0);
      });
      var totPay = 0, totRecv = 0;
      var sumBody = comps.map(function (c) {
        var b = byCo[c.id] || { pay: 0, recv: 0 };
        totPay += b.pay; totRecv += b.recv;
        return '<tr><td class="strong">' + esc(c.name) + '</td><td class="num">' + ui.money(b.pay) +
          '</td><td class="num">' + ui.money(b.recv) + '</td><td class="num">' + ui.money(b.recv - b.pay) + '</td></tr>';
      }).join('');
      sumBody += '<tr class="total"><td>Group</td><td class="num">' + ui.money(totPay) + '</td><td class="num">' +
        ui.money(totRecv) + '</td><td class="num">' + ui.money(totRecv - totPay) + '</td></tr>';

      var today = new Date(); today.setHours(0, 0, 0, 0);
      var detail = open.slice().sort(function (a, b) { return String(a.due).localeCompare(String(b.due)); })
        .map(function (s) {
          var due = new Date(String(s.due || '') + 'T00:00:00');
          var days = isNaN(due) ? 0 : Math.floor((today.getTime() - due.getTime()) / 86400000);
          var age = days > 0 ? days + 'd overdue' : 'current';
          return '<tr><td>' + esc(s.id) + '</td><td>' + esc(coShort(s.companyId)) + '</td><td>' + esc(s.party || '') +
            '</td><td>' + esc(s.kind || '') + '</td><td class="num">' + ui.money(s.amount) + '</td><td>' + ui.date(s.due) +
            '</td><td>' + esc(s.status || '') + '</td><td>' + age + '</td></tr>';
        }).join('');

      dl('group-payables-receivables.html', htmlDoc('Payables & Receivables Summary',
        '<h2>Outstanding by Concern (unpaid schedules)</h2>' +
        '<table><tr><th>Concern</th><th class="num">Payables</th><th class="num">Receivables</th><th class="num">Net Position</th></tr>' + sumBody + '</table>' +
        '<h2>Open Schedule Detail · ' + open.length + ' items</h2>' +
        '<table><tr><th>Ref</th><th>Company</th><th>Party</th><th>Type</th><th class="num">Amount</th>' +
        '<th>Due</th><th>Status</th><th>Aging</th></tr>' + detail + '</table>'));
    }
  } });

})(window.EPAL = window.EPAL || {});
